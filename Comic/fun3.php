        -Đồ ngốc! Mày ngủ say, chúng nó khiêng chõng mày nằm ra một bên, dắt bò ra rồi lại khiêng về chỗ cũ...

        Người kia vỡ lẽ nói:

        -À, thế ra quan thông đồng với bọn trộm, nên mới tỏ tường được như thế chứ !
